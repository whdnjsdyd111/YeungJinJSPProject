package main.bean;

public class JoinMemberAdminDataBean {
	private String admin_name;
	private String mem_email;
	private String admin_reg_date;
	private String mem_nickname;
	private String admin_phone;
	private int mem_level;
	
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String admin_name) {
		this.admin_name = admin_name;
	}
	public String getMem_email() {
		return mem_email;
	}
	public void setMem_email(String mem_email) {
		this.mem_email = mem_email;
	}
	public String getAdmin_reg_date() {
		return admin_reg_date;
	}
	public void setAdmin_reg_date(String admin_reg_date) {
		this.admin_reg_date = admin_reg_date;
	}
	public String getMem_nickname() {
		return mem_nickname;
	}
	public void setMem_nickname(String mem_nickname) {
		this.mem_nickname = mem_nickname;
	}
	public int getMem_level() {
		return mem_level;
	}
	public void setMem_level(int mem_level) {
		this.mem_level = mem_level;
	}
	public String getAdmin_phone() {
		return admin_phone;
	}
	public void setAdmin_phone(String admin_phone) {
		this.admin_phone = admin_phone;
	}
	
	
}
